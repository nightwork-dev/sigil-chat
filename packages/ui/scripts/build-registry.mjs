#!/usr/bin/env node

import { execFileSync } from "node:child_process"
import fs from "node:fs/promises"
import path from "node:path"
import { fileURLToPath } from "node:url"

import { buildLlmsTxt, buildRegistry, transformSourceImports } from "./registry-lib.mjs"

const __filename = fileURLToPath(import.meta.url)
const __dirname = path.dirname(__filename)
const packageRoot = path.resolve(__dirname, "..")
const srcRoot = path.join(packageRoot, "src")
const registryPath = path.join(packageRoot, "registry.json")
const componentMetaPath = path.join(packageRoot, "src/lib/component-meta.ts")
const packageJsonPath = path.join(packageRoot, "package.json")
const extrasPath = path.join(__dirname, "registry-extras.json")

async function main() {
  const [fileMap, packageJson, extras] = await Promise.all([
    readSourceFileMap(srcRoot), readJson(packageJsonPath), readJson(extrasPath).catch(() => ({ items: [], excludedItems: [] })),
  ])
  const registry = buildRegistry(fileMap, { packageJson, logger: console, excludedItems: extras.excludedItems ?? [] })
  // Hand-authored items the walker can't infer (e.g. the theme CSS, which
  // deliberately lives app-side — see themes.css header). Merged and re-sorted
  // so output stays deterministic; a name collision with a generated item is
  // an authoring error, not a merge preference.
  for (const item of extras.items) {
    if (registry.items.some((existing) => existing.name === item.name)) {
      throw new Error(`registry-extras.json item "${item.name}" collides with a generated item`)
    }
    registry.items.push(item)
  }
  registry.items.sort((a, b) => (a.name < b.name ? -1 : a.name > b.name ? 1 : 0))
  for (const omitted of extras.excludedItems ?? []) {
    console.log(`[registry] OMIT ${omitted.name}: ${omitted.reason}`)
    await fs.rm(path.resolve(packageRoot, `../../apps/web/public/r/${omitted.name}.json`), { force: true })
  }
  await writeStagingCopies(registry, fileMap)
  const generatedAt = new Date().toISOString()
  await fs.writeFile(registryPath, `${JSON.stringify(registry, null, 2)}\n`, "utf8")
  await fs.writeFile(componentMetaPath, buildComponentMetaModule(registry, generatedAt), "utf8")
  // llms.txt (llmstxt.org) for the public site — generated alongside the
  // registry so it can never go stale relative to the item list.
  const llmsPath = path.resolve(packageRoot, "../../apps/web/public/llms.txt")
  await fs.writeFile(llmsPath, `${buildLlmsTxt(registry)}\n`, "utf8")
  console.log(
    `Wrote ${path.relative(process.cwd(), registryPath)} with ${registry.items.length} items (+ llms.txt, ${path.relative(process.cwd(), componentMetaPath)})`,
  )
}

/**
 * shadcn build inlines file content verbatim, so import rewriting has to
 * happen between the real source and what the registry serves: every
 * generated item file is written as a transformed copy (imports rewritten to
 * the `@/registry/...` convention the CLI's transformImport understands)
 * under .registry-staging/, and the item paths are repointed there. Component
 * source on disk is never modified. Extras items (paths outside src/, e.g.
 * the app-side theme CSS) are left untouched — registry:file content ships
 * verbatim by design.
 */
async function writeStagingCopies(registry, fileMap) {
  const stagingRoot = path.join(packageRoot, ".registry-staging")
  await fs.rm(stagingRoot, { recursive: true, force: true })

  for (const item of registry.items) {
    for (const file of item.files) {
      const source = fileMap[file.path]
      if (source == null) continue // extras item — path not under src/
      const stagingRelPath = posixJoin(".registry-staging", file.path)
      const absolute = path.join(packageRoot, stagingRelPath)
      await fs.mkdir(path.dirname(absolute), { recursive: true })
      await fs.writeFile(absolute, transformSourceImports(source), "utf8")
      file.path = stagingRelPath
    }
  }
}

function buildComponentMetaModule(registry, generatedAt) {
  const entries = []

  for (const item of registry.items) {
    const sourcePath = sourcePathForRegistryItem(item)
    if (!sourcePath) continue
    entries.push([item.name, gitAddedAtForPath(sourcePath, generatedAt)])
  }

  entries.sort(([a], [b]) => a.localeCompare(b))

  const lines = [
    "// AUTO-GENERATED by scripts/build-registry.mjs — do not edit by hand.",
    "export interface ComponentMeta { addedAt: string }",
    `export const COMPONENT_META_GENERATED_AT = ${JSON.stringify(generatedAt)}`,
    "export const COMPONENT_META: Record<string, ComponentMeta> = {",
  ]

  for (const [name, addedAt] of entries) {
    lines.push(`  ${JSON.stringify(name)}: { addedAt: ${JSON.stringify(addedAt)} },`)
  }

  lines.push(
    "}",
    "const NEW_WINDOW_HOURS = 24",
    "/** True if the component was first added within NEW_WINDOW_HOURS (24h) of the build (generatedAt). SSR-safe: compares only baked timestamps, never Date.now(). */",
    "export function isNew(name: string, referenceIso: string = COMPONENT_META_GENERATED_AT): boolean {",
    "  const meta = COMPONENT_META[name]",
    "  if (!meta) return false",
    "",
    "  const addedAtMs = Date.parse(meta.addedAt)",
    "  const referenceMs = Date.parse(referenceIso)",
    "  if (!Number.isFinite(addedAtMs) || !Number.isFinite(referenceMs)) return false",
    "",
    "  const ageMs = referenceMs - addedAtMs",
    "  return ageMs >= 0 && ageMs <= NEW_WINDOW_HOURS * 60 * 60 * 1000",
    "}",
    "",
  )

  return `${lines.join("\n")}\n`
}

function sourcePathForRegistryItem(item) {
  const firstPath = item.files?.[0]?.path
  if (typeof firstPath !== "string") return null

  const sourcePath = firstPath.startsWith(".registry-staging/") ? firstPath.slice(".registry-staging/".length) : firstPath
  return sourcePath.startsWith("src/") ? sourcePath : null
}

function gitAddedAtForPath(relPath, generatedAt) {
  let output = ""

  try {
    output = execFileSync(
      "git",
      [
        "-C",
        packageRoot,
        "log",
        "--diff-filter=A",
        "--follow",
        "--format=%aI",
        "--no-patch",
        "--diff-merges=first-parent",
        "--",
        relPath,
      ],
      { encoding: "utf8" },
    )
  } catch {
    return generatedAt
  }

  const dates = output
    .split(/\r?\n/)
    .map((line) => line.trim())
    .filter((line) => /^\d{4}-\d{2}-\d{2}T/.test(line))

  return dates.at(-1) ?? generatedAt
}

function posixJoin(...parts) {
  return parts.join("/")
}

async function readSourceFileMap(rootDir) {
  const fileMap = {}

  async function walk(dir) {
    const entries = await fs.readdir(dir, { withFileTypes: true })
    entries.sort((a, b) => a.name.localeCompare(b.name))

    for (const entry of entries) {
      const absolutePath = path.join(dir, entry.name)
      if (entry.isDirectory()) {
        await walk(absolutePath)
        continue
      }

      if (!entry.isFile() || !/\.(css|ts|tsx)$/.test(entry.name)) continue

      const relPath = path.relative(packageRoot, absolutePath).split(path.sep).join("/")
      fileMap[relPath] = await fs.readFile(absolutePath, "utf8")
    }
  }

  await walk(rootDir)
  return fileMap
}

async function readJson(filePath) {
  return JSON.parse(await fs.readFile(filePath, "utf8"))
}

main().catch((error) => {
  console.error(error instanceof Error ? error.message : String(error))
  process.exitCode = 1
})
